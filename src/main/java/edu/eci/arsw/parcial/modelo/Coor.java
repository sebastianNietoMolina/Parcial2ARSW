package edu.eci.arsw.parcial.modelo;

public class Coor {
    private double lon;
    private double lat;

    public Coor() {
    }

    public double getLon() {
        return lon;
    }

    public void setLon(double lon) {
        this.lon = lon;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }
}
